#ifndef nowdb_pplru_decl
#define nowdb_pplru_decl

#include <nowdb/types/types.h>
#include <nowdb/types/error.h>
#include <nowdb/task/lock.h>

#include <tsalgo/lru.h>

/* ------------------------------------------------------------------------
 * Private Page LRU
 * ------------------------------------------------------------------------
 */
typedef ts_algo_lru_t nowdb_pplru_t;

/* ------------------------------------------------------------------------
 * Init LRU
 * ------------------------------------------------------------------------
 */
nowdb_err_t nowdb_pplru_init(nowdb_pplru_t *lru,
                             uint32_t       max);

/* ------------------------------------------------------------------------
 * Destroy LRU
 * ------------------------------------------------------------------------
 */
void nowdb_pplru_destroy(nowdb_pplru_t *lru);

/* ------------------------------------------------------------------------
 * Get data from LRU
 * ------------------------------------------------------------------------
 */
nowdb_err_t nowdb_pplru_get(nowdb_pplru_t *lru,
                            nowdb_pageid_t pge,
                            char        **page);

/* ------------------------------------------------------------------------
 * Add data to LRU
 * ------------------------------------------------------------------------
 */
nowdb_err_t nowdb_pplru_add(nowdb_pplru_t *lru,
                            nowdb_pageid_t pge,
                            char         *page);

#endif
