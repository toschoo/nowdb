#ifndef nowdb_pklru_decl
#define nowdb_pklru_decl

#include <nowdb/types/types.h>
#include <nowdb/types/error.h>
#include <nowdb/task/lock.h>

#include <tsalgo/lru.h>

/* ------------------------------------------------------------------------
 * Private Text LRU
 * ------------------------------------------------------------------------
 */
typedef ts_algo_lru_t nowdb_pklru_t;

/* ------------------------------------------------------------------------
 * Init LRU
 * ------------------------------------------------------------------------
 */
nowdb_err_t nowdb_pklru_init(nowdb_pklru_t *lru,
                             uint32_t       max);

/* ------------------------------------------------------------------------
 * Destroy LRU
 * ------------------------------------------------------------------------
 */
void nowdb_pklru_destroy(nowdb_pklru_t *lru);

/* ------------------------------------------------------------------------
 * Get data from LRU
 * ------------------------------------------------------------------------
 */
nowdb_err_t nowdb_pklru_get(nowdb_pklru_t *lru,
                            char          *str,
                            nowdb_key_t   *key,
                            char        *found);

/* ------------------------------------------------------------------------
 * Add data to LRU
 * ------------------------------------------------------------------------
 */
nowdb_err_t nowdb_pklru_add(nowdb_pklru_t *lru,
                            char          *str,
                            nowdb_key_t    key);

#endif
