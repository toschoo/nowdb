#ifndef nowdb_ptlru_decl
#define nowdb_ptlru_decl

#include <nowdb/types/types.h>
#include <nowdb/types/error.h>
#include <nowdb/task/lock.h>

#include <tsalgo/lru.h>

/* ------------------------------------------------------------------------
 * Private Text LRU
 * ------------------------------------------------------------------------
 */
typedef ts_algo_lru_t nowdb_ptlru_t;

/* ------------------------------------------------------------------------
 * Init LRU
 * ------------------------------------------------------------------------
 */
nowdb_err_t nowdb_ptlru_init(nowdb_ptlru_t *lru,
                             uint32_t       max);

/* ------------------------------------------------------------------------
 * Destroy LRU
 * ------------------------------------------------------------------------
 */
void nowdb_ptlru_destroy(nowdb_ptlru_t *lru);

/* ------------------------------------------------------------------------
 * Get data from LRU
 * ------------------------------------------------------------------------
 */
nowdb_err_t nowdb_ptlru_get(nowdb_ptlru_t *lru,
                            nowdb_key_t    key,
                            char         **str);

/* ------------------------------------------------------------------------
 * Add data to LRU
 * ------------------------------------------------------------------------
 */
nowdb_err_t nowdb_ptlru_add(nowdb_ptlru_t *lru,
                            nowdb_key_t    key,
                            char          *str);

#endif
